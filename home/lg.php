				<h2>LG anuncia TV gamer curva que pode ser dobrada</h2>
				<span class="data-postagem">Postado dia 01/09/2022 por MARCELO JUNIOR</span>
				<img src="gamer.jpg" width="640px" height="264px">
				<p>Na era dos smartphones dobráveis, a LG apresentou uma nova inovação durante sua conferência na IFA 2022: uma TV gamer OLED de 42 polegadas que pode ser dobrada pelo usuário.<br><br>
				Essa não é a primeira vez que a companhia se destaca nesse segmento e, aliás, é uma das principais marcas no desenvolvimento de displays flexíveis da indústria. Há poucos dias, a Corsair anunciou um produto parecido, um monitor gamer dobrável com tecnologia da LG.<br><br>
				Chamada de LG OLED Flex LX3, a TV tem 20 tipos de ajustes de curvatura de imagem, podendo ir de um display totalmente plano até uma tela curva de 900R, que deve englobar boa parte do campo de visão do jogador.<br><br>
				O aparelho possui uma ferramenta externa na parte traseira que permite ser dobrada, e segundo a marca, não compromete a qualidade de imagem durante a transformação do plano para o curvo. Aliás, essa mudança pode ser feita de forma manual ou através de um comando no controle remoto. A área da LG LX3 pode ser ajustada para diferentes tipos de games, com opções de redução de tamanho para 27 ou 32 polegadas.</p>
				<img src="lg.jpg" width="440px" height="440px">
				<p>Dentre as especificações, o modelo opera em resolução 4K com taxa de atualização de 120Hz, e porta HDMI 2.1, suportando os consoles atuais. Na parte frontal, ainda há dois alto-falantes com 40W de potência e integração com Dolby Atmos.<br><br>
				Por ser smart, o televisor também tem suporte a um HUB central da LG para o usuário assistir streamings como Netflix, HBO Max, Prime Video, etc, além de serviços de jogos em nuvem e outras interações.<br><br>
				Por enquanto a LG OLED Flex LX3 não teve data ou preços revelados, e não tem nenhuma previsão para chegar ao varejo internacional.</p>
